package com.tr.deneme;

public class SayfaElemanlar {
	
	String uretici;
	String model;
	String modelTipi;
	String motorKodu;
	String kw;
	String ps;
	String uretimSuresi;
	String hava;
	String yag;
	String yakıt;
	String icMekan;
	String diger;
	
	
	public SayfaElemanlar(String uretici, String model, String modelTipi, String motorKodu, String kw, String ps,
			String uretimSuresi, String hava, String yag, String yakıt, String icMekan, String diger) {
		super();
		this.uretici = uretici;
		this.model = model;
		this.modelTipi = modelTipi;
		this.motorKodu = motorKodu;
		this.kw = kw;
		this.ps = ps;
		this.uretimSuresi = uretimSuresi;
		this.hava = hava;
		this.yag = yag;
		this.yakıt = yakıt;
		this.icMekan = icMekan;
		this.diger = diger;
	}
	public String getUretici() {
		return uretici;
	}
	public void setUretici(String uretici) {
		this.uretici = uretici;
	}
	public String getModel() {
		return model;
	}
	public void setModel(String model) {
		this.model = model;
	}
	public String getModelTipi() {
		return modelTipi;
	}
	public void setModelTipi(String modelTipi) {
		this.modelTipi = modelTipi;
	}
	public String getMotorKodu() {
		return motorKodu;
	}
	public void setMotorKodu(String motorKodu) {
		this.motorKodu = motorKodu;
	}
	public String getKw() {
		return kw;
	}
	public void setKw(String kw) {
		this.kw = kw;
	}
	public String getPs() {
		return ps;
	}
	public void setPs(String ps) {
		this.ps = ps;
	}
	public String getUretimSuresi() {
		return uretimSuresi;
	}
	public void setUretimSuresi(String uretimSuresi) {
		this.uretimSuresi = uretimSuresi;
	}
	public String getHava() {
		return hava;
	}
	public void setHava(String hava) {
		this.hava = hava;
	}
	public String getYag() {
		return yag;
	}
	public void setYag(String yag) {
		this.yag = yag;
	}
	public String getYakıt() {
		return yakıt;
	}
	public void setYakıt(String yakıt) {
		this.yakıt = yakıt;
	}
	public String getIcMekan() {
		return icMekan;
	}
	public void setIcMekan(String icMekan) {
		this.icMekan = icMekan;
	}
	public String getDiger() {
		return diger;
	}
	public void setDiger(String diger) {
		this.diger = diger;
	}
	
	
	
	

}
